    <p>
        <strong>I/ Quy Định Đổi Hàng.</strong> <br>

        Chính sách áp dụng: Áp dụng 01 lần đổi/01 đơn hàng.<br>

        - Sản phẩm nguyên giá được đổi sang sản phẩm nguyên giá khác còn hàng. Khách hàng thanh toán số tiền chênh lệch nếu giá trị sản phẩm đổi lớn hơn. Không hoàn trả lại tiền thừa dưới bất kỳ hình thức nào.<br>

        - Sản phẩm giảm giá đến 30% được đổi màu/size (nếu còn hàng) hoặc theo quy chế từng chương trình (nếu có) - Không hỗ trợ đổi sang sản phẩm khác.<br>

        - Sản phẩm không áp dụng đổi bao gồm: Phụ kiện, đồ lót, sản phẩm giảm giá từ 50%.<br>

        - Sản phẩm chỉ được đổi một lần duy nhất.<br>

        Điều kiện đổi sản phẩm:<br>

        - Đổi hàng trong vòng 03 ngày kể từ ngày khách hàng nhận được sản phẩm<br>

        - Sản phẩm còn trong tình trạng ban đầu khi nhận hàng, còn nguyên tem và nhãn mác.<br>

        - Sản phẩm chưa qua giặt ủi hoặc bẩn, bị hư hỏng.<br>

       <strong> II/ Quy Định Trả Hàng.</strong> <br>

        Chính sách áp dụng:<br>

        - Khách hàng được trả sản phẩm trong trường hợp có lỗi phát sinh từ nhà sản xuất và không có nhu cầu đổi sang sản phẩm khác.<br>

        - Các trường hợp lỗi do nhà sản xuất gồm:  phai màu, lỗi chất liệu, lỗi đường may…<br>

        - Hoàn tiền lại sản phẩm gặp lỗi qua tài khoản ngân hàng.<br>

        - Pantio chịu 100% chi phí vận chuyển trả lại hàng.<br>

        - Pantio sẽ xử lý trong vòng 10 ngày kể từ ngày nhận được sản phẩm lỗi.<br>

        Điều kiện trả sản phẩm<br>

           Trả sản phẩm trong vòng 05 ngày kể từ ngày nhận sản phẩm.<br>

        Chính sách hoàn tiền.<br>

        - Đối với trường hợp thanh toán trước, khách hàng sẽ được hoàn tiền khi hàng nhận bị lỗi do sản xuất và khách hàng trả hàng không có nhu cầu đổi sang sản phẩm khác.<br>

        - Thời gian hoàn tiền: từ 07 đến 15 ngày kể từ khi Pantio nhận được hàng trả từ khách hàng. Tiền được hoàn vào tài khoản cá nhân của khách hàng cung cấp.<br>
        
